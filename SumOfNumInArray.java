
public class SumOfNumInArray {

	public static void main(String[] args) {

		System.out.println("MMS");
		
		int[] num= {12, 23, 23, 45, 56, 57};
		
		int sum=0;
		//for-each-loop
		for(int n : num) {
			sum +=n;
		}
		System.out.println("Sum of all Numbers = "+sum);
		System.out.println("MME");
	}

}
